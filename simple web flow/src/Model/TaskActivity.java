package Model;


public class TaskActivity {	
	
	public int task;
	public String activites;
	
	public int getTask() {
		return task;
	}
	public void setTask(int task) {
		this.task = task;
	}
	public String getActivites() {
		return activites;
	}
	public void setActivites(String activites) {
		this.activites = activites;
	}
}
